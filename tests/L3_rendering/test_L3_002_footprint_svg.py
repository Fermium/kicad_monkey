def get_test_footprints() -> List[Path]:
    """Get all footprint files in input directory."""
    if not INPUT_DIR.exists():
        return []
    return sorted(INPUT_DIR.glob("*.kicad_mod"))


def normalize_path_data(path_d: str, precision: int = 2) -> Set[Tuple[float, ...]]:
    """
    Normalize SVG path data to a set of coordinate tuples.

    This allows comparing paths regardless of:
    - Starting point (paths can start at any corner)
    - Whitespace/formatting differences
    - Absolute offset (different bounding box calculations)
    - Minor floating point precision differences

    Args:
        path_d: SVG path d attribute value
        precision: Decimal places for rounding (default 2 for 0.01mm tolerance)

    Returns set of coordinate tuples for comparison, centered at origin.
    """
    # Extract all coordinates from path
    coords = []
    # Match numbers (including negative and decimal)
    numbers = re.findall(r'-?\d+\.?\d*', path_d)
    for i in range(0, len(numbers) - 1, 2):
        x = float(numbers[i])
        y = float(numbers[i + 1])
        coords.append((x, y))

    if not coords:
        return set()

    # Center the coordinates at origin to normalize for different bounding boxes
    # This allows comparison even when viewBox offsets differ
    min_x = min(c[0] for c in coords)
    min_y = min(c[1] for c in coords)

    centered = [(round(x - min_x, precision), round(y - min_y, precision)) for x, y in coords]
    return set(centered)


def extract_paths_from_svg(svg_content: str) -> List[str]:
    """Extract path-like coordinate streams from SVG."""
    paths = re.findall(r'<path\b[^>]*\sd="([^"]+)"', svg_content)
    point_shapes = re.findall(r'<(?:polygon|polyline)\b[^>]*\spoints="([^"]+)"', svg_content)
    return paths + point_shapes


def extract_circles_from_svg(svg_content: str) -> List[Tuple[float, float, float]]:
    """Extract all circle elements from SVG as (cx, cy, r) tuples."""
    circles = []
    # Match circle elements
    pattern = r'<circle\s+cx="([^"]+)"\s+cy="([^"]+)"\s+r="([^"]+)"'
    for match in re.finditer(pattern, svg_content):
        cx = float(match.group(1))
        cy = float(match.group(2))
        r = float(match.group(3))
        circles.append((cx, cy, r))
    return circles


def count_svg_shapes(svg_content: str) -> dict:
    """Count all shape types in SVG."""
    return {
        'paths': len(extract_paths_from_svg(svg_content)),
        'circles': len(extract_circles_from_svg(svg_content)),
    }


def compare_svg_paths(our_svg: str, ref_svg: str) -> Tuple[bool, str]:
    """
    Compare SVG shapes between our output and reference.

    Handles:
    - Paths (our filled polygons and stroked lines)
    - Circles (KiCad native circles)
    - Our circle polygons vs KiCad native circles

    Returns (match, message) tuple.
    """
    our_paths = extract_paths_from_svg(our_svg)
    our_circles = extract_circles_from_svg(our_svg)
    ref_paths = extract_paths_from_svg(ref_svg)
    ref_circles = extract_circles_from_svg(ref_svg)

    # Total shape count (we render circles as paths, KiCad uses native circles)
    our_total = len(our_paths) + len(our_circles)
    ref_total = len(ref_paths) + len(ref_circles)

    # If reference has circles and we have equivalent number of polygon paths,
    # that's acceptable (we render circles as polygon approximations)
    if ref_circles and not our_circles:
        # Check if our extra paths could be circle approximations
        expected_paths = len(ref_paths)
        circle_equivalent_paths = len(ref_circles)
        if len(our_paths) == expected_paths + circle_equivalent_paths:
            # Assume our extra paths are circle approximations - this is OK
            our_paths = our_paths[:expected_paths]  # Compare only the non-circle paths

    if len(our_paths) != len(ref_paths):
        return False, f"Path count mismatch: ours={len(our_paths)}, ref={len(ref_paths)} (circles: ours={len(our_circles)}, ref={len(ref_circles)})"

    our_path_keys = Counter(
        tuple(sorted(normalize_path_data(path)))
        for path in our_paths
    )
    ref_path_keys = Counter(
        tuple(sorted(normalize_path_data(path)))
        for path in ref_paths
    )
    if our_path_keys != ref_path_keys:
        return False, f"Path coords mismatch:\n  ours: {our_path_keys}\n  ref: {ref_path_keys}"

    if len(our_circles) != len(ref_circles):
        return False, f"Circle count mismatch: ours={len(our_circles)}, ref={len(ref_circles)}"

    return True, "All paths match"


