class TestFootprintSvgGeneration:
    """Tests for footprint SVG generation."""

    def test_simple_footprint_generates_svg(self):
        """Test that a simple footprint generates valid SVG."""
        from kicad_monkey import KiCadFootprint

        # Use a simple test footprint
        fp_files = get_test_footprints()
        if not fp_files:
            pytest.skip("No test footprints available")

        fp = KiCadFootprint.from_file(fp_files[0])
        svg = fp.to_svg(layers=["F.Cu"])

        assert svg is not None
        assert "<?xml version" in svg
        assert "<svg" in svg
        assert "</svg>" in svg

    def test_layer_filter_works(self):
        """Test that layer filter only includes requested layers."""
        from kicad_monkey import KiCadFootprint

        fp_files = get_test_footprints()
        if not fp_files:
            pytest.skip("No test footprints available")

        fp = KiCadFootprint.from_file(fp_files[0])

        # Generate SVG for single layer
        svg_fcu = fp.to_svg(layers=["F.Cu"])
        svg_bcu = fp.to_svg(layers=["B.Cu"])

        # Both should be valid SVGs
        assert "<svg" in svg_fcu
        assert "<svg" in svg_bcu


class TestFootprintSvgComparison:
    """Tests comparing our SVG output against KiCad CLI references."""

    @pytest.fixture
    def ensure_output_dir(self):
        """Ensure output directory exists."""
        OUTPUT_DIR.mkdir(parents=True, exist_ok=True)
        return OUTPUT_DIR

    @pytest.mark.parametrize("fp_path", get_test_footprints(),
                             ids=lambda p: p.stem)
    def test_fcu_layer_matches_reference(self, fp_path, ensure_output_dir):
        """Test that F.Cu layer SVG matches reference."""
        from kicad_monkey import KiCadFootprint

        layer = "F.Cu"
        layer_safe = layer.replace(".", "_")

        # Load footprint
        fp = KiCadFootprint.from_file(fp_path)

        # Check if reference exists
        ref_path = REFERENCE_DIR / f"{fp.name}__{layer_safe}.svg"
        if not ref_path.exists():
            pytest.fail(
                f"Missing reference SVG for {fp.name} {layer}: {ref_path}. "
                "Run tools/kicad/tests/generate_svg_references.py to regenerate references."
            )

        # Generate our SVG
        our_svg = fp.to_svg(layers=[layer])

        # Save our output for debugging
        out_path = ensure_output_dir / f"{fp.name}__{layer_safe}.svg"
        out_path.write_text(our_svg)

        # Load reference
        ref_svg = ref_path.read_text()

        # Compare paths
        match, msg = compare_svg_paths(our_svg, ref_svg)

        if not match:
            log.error(f"SVG mismatch for {fp.name} {layer}: {msg}")
            log.error(f"Our output: {out_path}")
            log.error(f"Reference: {ref_path}")

        assert match, f"SVG mismatch for {fp.name} {layer}: {msg}"


class TestPathNormalization:
    """Tests for path normalization utilities."""

    def test_normalize_simple_rect(self):
        """Test normalizing a simple rectangle path."""
        path1 = "M 0,0 L 1,0 L 1,1 L 0,1 Z"
        path2 = "M 1,1 L 0,1 L 0,0 L 1,0 Z"  # Same rect, different start

        coords1 = normalize_path_data(path1)
        coords2 = normalize_path_data(path2)

        # Both should have same coordinate set
        assert coords1 == coords2

    def test_normalize_with_decimals(self):
        """Test normalizing path with decimal coordinates."""
        path = "M 0.6600,0.7400 0.0000,0.7400 0.0000,0.0000 0.6600,0.0000 Z"
        coords = normalize_path_data(path)

        expected = {(0.66, 0.74), (0.0, 0.74), (0.0, 0.0), (0.66, 0.0)}
        assert coords == expected


