def _iter_board_svg_cases() -> List[dict]:
    """Manifest-driven enumeration of synthetic PCB foundation cases.

    Yields the synthetic ``pcb_foundation`` cases that participate in the
    ``board_svg`` domain. Real-world ``projects/*`` cases also have the
    ``board_svg`` domain but are excluded here pending the legacy
    renderer divergence fix (see KICAD_MONKEY_REWORK_PLAN). Synthetic-only
    scope matches the prior local-glob behaviour exactly.
    """
    cases: list[dict] = []
    for case in iter_kicad_corpus_cases(domain="board_svg"):
        if case.get("origin") != "synthetic":
            continue
        if case.get("layout") != "case_bucket":
            continue
        cases.append(case)
    return cases


