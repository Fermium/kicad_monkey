class TestBoardSvgComparison:
    """Tests comparing our SVG output against KiCad CLI references."""

    @pytest.mark.parametrize("test_case", get_test_cases(), ids=_make_test_id)
    def test_layer_matches_reference(self, test_case):
        """Test that layer SVG matches reference."""
        from kicad_monkey import KiCadPcb

        board_path, layer = test_case
        board_name = board_path.stem
        layer_safe = layer_filename_token(layer)

        # Skip known failing tests
        if (board_name, layer) in SKIP_TESTS:
            pytest.skip(f"Known issue: {board_name} {layer}")

        # Load board
        pcb = KiCadPcb.from_file(board_path)

        # Reference SVG lives alongside the input: <case>/reference_output/<descriptor>__<layer>.svg.
        ref_path = board_path.parent.parent / "reference_output" / f"{board_name}__{layer_safe}.svg"
        if not ref_path.exists():
            pytest.skip(f"No reference SVG for {board_name} {layer}")
        ref_svg = ref_path.read_text()
        if not ref_svg.strip():
            pytest.skip(f"Empty reference SVG for {board_name} {layer}")

        # Determine layers to render
        if layer == "All Layers":
            # Render all visible layers combined
            layers_to_render = ALL_LAYERS_LIST
        else:
            # For individual layers, include Edge.Cuts for reference
            layers_to_render = [layer]
            if layer != "Edge.Cuts":
                layers_to_render.append("Edge.Cuts")

        # Generate our SVG
        our_svg = pcb.to_svg(layers=layers_to_render)

        # Save our output for debugging in the corpus-local case output folder.
        out_folder = _board_svg_output_dir_for_board_path(board_path)
        out_folder.mkdir(parents=True, exist_ok=True)
        out_path = out_folder / f"{board_name}__{layer_safe}.svg"
        out_path.write_text(our_svg)

        # Compare shapes with tighter default threshold and only explicit
        # lower gates for documented real-world text tessellation noise.
        min_match_pct = match_threshold_for(board_name, layer)
        match, msg = compare_svg_shapes(our_svg, ref_svg, min_match_pct=min_match_pct)

        if not match:
            log.error(f"SVG mismatch for {board_name} {layer}: {msg}")
            log.error(f"Our output: {out_path}")
            log.error(f"Reference: {ref_path}")

        assert match, f"SVG mismatch for {board_name} {layer}: {msg}"


